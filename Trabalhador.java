public class Trabalhador {
    String nome;
    int idade, horasTrabalhadas;
    double salario, salarioAtual;

    public String toString() {
        return "Nome do Trabalhador: " + nome + "\n" +
                "idade do Trabalhador: " + idade + "\n" +
                "horas trabalhadas neste mês: " + horasTrabalhadas + "\n" +
                "salário: " + salario + "\n" +
                "salário atual: " + salarioAtual;

    }
}
